// Looping in Java
// Printing numbers 0-4

class MyClass {
    //contents of the class (fields and methods)
    public static void main(String[] args){

        // code to be executed
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        };
      }

 }

 // to run: 
 // install a JDK 
 // run "java loop.java"